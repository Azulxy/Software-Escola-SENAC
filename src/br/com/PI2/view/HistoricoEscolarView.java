    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        String codigoMatricula = jTextFieldMatriculaAluno.getText();
        Aluno aluno = alunoRepository.buscarAlunoPorCodigoMatricula(codigoMatricula);

        if (aluno != null) {
            // Preencher a tabela com as notas do aluno
            jTableTabela.setModel(criarTabela(codigoMatricula));
        } else {
            JOptionPane.showMessageDialog(this, "Aluno não encontrado.");
        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed
   
