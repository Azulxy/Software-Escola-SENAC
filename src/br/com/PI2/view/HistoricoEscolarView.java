    private void jButtonAtribuirNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtribuirNotaActionPerformed
        // Obtém o código de matrícula do aluno do jTextFieldMatriculaAluno
        String codigoMatricula = jTextFieldMatriculaAluno.getText();

        // Verifica se o código de matrícula não está vazio
        if (!codigoMatricula.isEmpty()) {
            // Abre a AtribuirNotaView passando o código de matrícula do aluno
            AtribuirNotaView atribuirNotaView = new AtribuirNotaView(codigoMatricula);
            atribuirNotaView.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, insira um código de matrícula válido.");
        }
    }//GEN-LAST:event_jButtonAtribuirNotaActionPerformed

