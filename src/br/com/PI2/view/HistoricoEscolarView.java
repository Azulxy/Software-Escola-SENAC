    public DefaultTableModel criarTabela(String codigoMatricula) {
        Aluno aluno = alunoRepository.buscarAlunoPorCodigoMatricula(codigoMatricula);
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Disciplina");
        model.addColumn("Nota");

        if (aluno != null) {
        // Adiciona as notas do aluno ao modelo de tabela
        model.addRow(new Object[]{"Matemática", aluno.getNotaMatematica()});
        model.addRow(new Object[]{"Português", aluno.getNotaPortugues()});
        model.addRow(new Object[]{"Ciências", aluno.getNotaCiencias()});
        model.addRow(new Object[]{"História", aluno.getNotaHistoria()});
        } else {
        // Se o aluno não for encontrado, adiciona linhas vazias
        model.addRow(new Object[]{"Matemática", ""});
        model.addRow(new Object[]{"Português", ""});
        model.addRow(new Object[]{"Ciências", ""});
        model.addRow(new Object[]{"História", ""});
        }

    return model;
    }

