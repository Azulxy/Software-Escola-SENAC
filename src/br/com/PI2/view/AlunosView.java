    public DefaultTableModel criarModeloTabela() {
        ArrayList<Aluno> listaAlunos = POJO.AlunoRepository.listarAlunos();
        String[] colunas = {"Código de matrícula", "Nome", "Telefone", "Série"};
        DefaultTableModel tabela = new DefaultTableModel(colunas, 0);
        for (Aluno aluno : listaAlunos) {
            String[] linha = {
                aluno.getCodigoMatricula(),
                aluno.getNome(),
                aluno.getTelefone(),
                aluno.getSerie()
            };
            tabela.addRow(linha);
        }
        return tabela;
    }

