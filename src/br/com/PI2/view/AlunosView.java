    private void jButtonExcluirAlunoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirAlunoActionPerformed
        ArrayList<Aluno> listaAlunos = alunos.listarAlunos();
        
        // Obtém a linha selecionada na tabela
        int selectedRow = jTableTabela.getSelectedRow();
        if (selectedRow != -1) { // Se uma linha foi selecionada
            // Obtém o modelo da tabela
            DefaultTableModel model = (DefaultTableModel) jTableTabela.getModel();
            // Obtém o aluno correspondente à linha selecionada
            Aluno aluno = listaAlunos.get(selectedRow);
            // Remove o aluno do ArrayList
            alunos.removerAluno(aluno);
            // Remove a linha selecionada da tabela
            model.removeRow(selectedRow);
            
        } else {
            JOptionPane.showMessageDialog(this, "Selecione uma consulta para excluir.");
        }
    }//GEN-LAST:event_jButtonExcluirAlunoActionPerformed

